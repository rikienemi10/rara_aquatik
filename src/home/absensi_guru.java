    private void cariguruActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cariguruActionPerformed
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/raraaquatik","root","");
            java.sql.Statement stat = cn.createStatement();
            ResultSet hasil = stat.executeQuery("select * from dataguru where kode_guru = '"+kodeguru.getText()+"'");
            while(hasil.next()){
                String ng = hasil.getString("nama_guru");
                namaguru.setText(ng);
                namaguru.setEnabled(false);
            }
            tampilkan();
        }catch(Exception e){}
    }//GEN-LAST:event_cariguruActionPerformed

