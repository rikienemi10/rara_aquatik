    private void searchguruKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_searchguruKeyReleased
        try{
            Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/raraaquatik","root","");
            Statement st = (Statement) cn.createStatement();
            if(searchguru.getText().length() !=0) {
                ResultSet rs = st.executeQuery("select * from absenguru where kode_guru like '"+searchguru.getText()
                    +"%' or nama_guru like'"+searchguru.getText()
                    +"%' or tanggal_mengajar like'"+searchguru.getText()
                    +"%' or jam_mengajar like'"+searchguru.getText()
                    +"%' or nama_siswa like'"+searchguru.getText()     
                    +"%' or keterangan like'"+searchguru.getText()
                    +"%';"
                );
                tampilkan();
                reset();
            }else if (searchguru.getText().length() ==0) {
                ResultSet rs = st.executeQuery("select * from absenguru");
                tampilkan();
            }
        }catch (SQLException ex){
            JOptionPane.showMessageDialog(null, "Koneksi gagal!");

        }
    }//GEN-LAST:event_searchguruKeyReleased

