     private void tampilkan() {
        int row = tabelabsen.getRowCount();
    for (int a=0; a<row; a++){
        model.removeRow(0);
    }
    try{
        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/raraaquatik","root","");
        ResultSet rs = cn.createStatement().executeQuery("select * from absenguru where kode_guru like '"+searchguru.getText()
            +"%' or nama_guru like'"+searchguru.getText()    
            +"%' or tanggal_mengajar like'"+searchguru.getText()
            +"%' or jam_mengajar like'"+searchguru.getText()
            +"%' or nama_siswa like'"+searchguru.getText()    
            +"%' or keterangan like'"+searchguru.getText()
            +"%';"
            );
        while(rs.next()){
        String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6)};
        model.addRow(data);
        }
    }catch (SQLException ex) {
        
    }
    }
    
    private void tabelabsenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelabsenMouseClicked
        int i = tabelabsen.getSelectedRow();
        if (i>-1) {
            kodeguru.setText(model.getValueAt(i, 0).toString());
            namaguru.setText(model.getValueAt(i, 1).toString());
            try {
                DefaultTableModel mdl = (DefaultTableModel)tabelabsen.getModel();
                Date date = new SimpleDateFormat("dd-MM-yyyy").parse((String)mdl.getValueAt(i,2).toString());
                tglmengajar.setDate(date);
            } catch (ParseException ex) {
            }
            jamngajar.setText(model.getValueAt(i, 3).toString());
            namasiswa.setText(model.getValueAt(i, 4).toString());
            ket.setSelectedItem(model.getValueAt(i, 5).toString());
    }//GEN-LAST:event_tabelabsenMouseClicked
    }
