        Connection cn = DriverManager.getConnection("jdbc:mysql://localhost/raraaquatik","root","");
        ResultSet rs = cn.createStatement().executeQuery("select * from dataguru where kode_guru like '"+search.getText()
            +"%' or nama_guru like'"+search.getText()
            +"%' or tempat_lahir like'"+search.getText()
            +"%' or tanggal_lahir like'"+search.getText()
            +"%' or jenis_kelamin like'"+search.getText()
            +"%' or alamat like'"+search.getText()
            +"%' or no_telepon like'"+search.getText()
            +"%';"
            );
        while(rs.next()){
        String data [] = {rs.getString(1),rs.getString(2),rs.getString(3),rs.getString(4),rs.getString(5),rs.getString(6),rs.getString(7)};
        model.addRow(data);
        }
