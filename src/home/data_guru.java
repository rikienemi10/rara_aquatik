    private void tabelMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelMouseClicked
        int i = tabel.getSelectedRow();
        if (i>-1) {
        kodeguru.setText(model.getValueAt(i, 0).toString());
        namaguru.setText(model.getValueAt(i, 1).toString());
        tempatlahir.setText(model.getValueAt(i, 2).toString());
        try {
                DefaultTableModel mdl = (DefaultTableModel)tabel.getModel();
                Date date = new SimpleDateFormat("dd-MM-yyyy").parse((String)mdl.getValueAt(i,3).toString());
                tglahir.setDate(date);
            } catch (ParseException ex) {
            }
        String jk = model.getValueAt(i, 4).toString();
        if (jk.equals("Laki-Laki")){
            lakilaki.setSelected(true);
        }else if (jk.equals("Perempuan")) {
            perempuan.setSelected(true);
        }
        alamat.setText(model.getValueAt(i, 5).toString());
        notelepon.setText(model.getValueAt(i, 6).toString());
    }//GEN-LAST:event_tabelMouseClicked
    }
